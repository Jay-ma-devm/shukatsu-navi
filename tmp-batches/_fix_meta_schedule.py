def process_file(path):
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    for a in data:
        new_meta = expand_meta(a)
        a["meta_desc"] = new_meta
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"=== {path} ===")
    for a in data:
        print(f"#{a['article_no']} meta_len={len(a['meta_desc'])} : {a['meta_desc']}")


